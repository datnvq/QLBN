    public void ViewBA(){
        y = list.get(c);
        this.txtId.setText("" + y.getID());
        this.txtTrieuChung.setText(y.getTrieuChung());
        this.txtChuanDoan.setText(y.getChanDoan());
        this.txtChiDinh.setText(y.getChiDinh());
        this.txtKetLuan.setText(y.getKetLuan());
        this.txtDonThuoc.setText(y.getDonThuoc());
        this.txtDate.setDate(y.getNgayKham());
        
        OnOff(true, false);
    }
    public void ViewTableBA(){
        DefaultTableModel model = (DefaultTableModel) this.tblBenhAn2.getModel();
        model.setNumRows(0);
        int n = 1;
        for(BenhAn y : list){
            model.addRow(new Object[]{n++,y.getID(), y.getTrieuChung(), y.getChanDoan(), y.getChiDinh(), y.getKetLuan(), y.getDonThuoc(), y.getNgayKham()});
        }
        this.tblBenhAn2.setRowHeight(20);
    }
