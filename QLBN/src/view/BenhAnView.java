    private void btnSaveBAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveBAActionPerformed
        // TODO add your handling code here:
        int ID = Integer.parseInt(this.txtId.getText()) ;
        String TrieuChung = this.txtTrieuChung.getText();
        String ChuanDoan = this.txtChuanDoan.getText();
        String ChiDinh = this.txtChiDinh.getText();
        String KetLuan = this.txtKetLuan.getText();
        String DonThuoc = this.txtKetLuan.getText();
        Date NgayKham = this.txtDate.getDate();
        
        
        if(kt == 1){
            list.add(new BenhAn(ID,TrieuChung,ChuanDoan,ChiDinh,KetLuan,DonThuoc,NgayKham));
        }
        if(kt == -1){
            list.set(c,new BenhAn(ID,TrieuChung,ChuanDoan,ChiDinh,KetLuan,DonThuoc,NgayKham));
        }
        ViewBA();
        ViewTableBA();
    }//GEN-LAST:event_btnSaveBAActionPerformed

